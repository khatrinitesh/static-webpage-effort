// switch statements 1 
var floor = 4
switch(floor){
	case 5 : console.log('5th floor')
	break
	case 4 : console.log('4th floor')
	break;
	case 3 : console.log('3rd floor')
	break;
	case 2 : console.log('2nd floor')
	break;
	case 1 : console.log('1st floor')
	break;
	default: console.log('ground floor')
	break;
}